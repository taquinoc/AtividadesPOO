public class Medico {
    
    private String nome;
    private String proceedimentos;
    private String especialidade;

    
    
    
    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getProceedimentos() {
        return proceedimentos;
    }

    public void setProceedimentos(String proceedimentos) {
        this.proceedimentos = proceedimentos;
    }

    public String getEspecialidade() {
        return especialidade;
    }

    public void setEspecialidade(String especialidade) {
        this.especialidade = especialidade;
    }
    
    
}
